//eventos en javascript
// los eventos son acciones o sucesos que ocurren en el navegador web
// pueden ser detectados y manejados mediante codigo javascript.
// Estos eventos pueden ser desencadenados por diferentes acciones
// como al hacer click en un elemento de HTML, mover el cursor del mouse
// pulsar una tecla del teclado, cargar una pagina web. etc

//Listado de los eventos mas principales de javascript

//OK load -> se desencadena cuando se completa la carga de una pagina web
//OK click -> se activa cuando se hace click sobre un elemento HTML, como un boton o un enlace
//OK change -> se dispara cuando el valor de un elemento de un formulario cambia, como un campo de texto o un campo de tipo select
//OK submit -> se dispara cuando ocurre el envio de un formulario
//scroll -> se activa cuando el usaurio desplaza el contenido de una pagina web hacia arriba o abajo
//OK keydown, keyup, keypress , son eventos que se desencadenan cuando se presiona o suelta una tecla del teclado
//OK setTimeout -> estos eventos permiten ejecutar una función después de un periodo de tiempo. (espera para ejecutar)
//OK setInterval -> estos eventos permiten ejecutar una función después de un intervalo de tiempo. (se ejecuta cada x tiempo)
//OK resize -> se activa cuando se cambia el tamaño de la ventana del navegador
//drag drop -> son eventos que se utilizan para interactuar con elementos arrastrables y soltables de una pagina web
//OK dblclick -> se acuando cuando se hace doble click sobre un elemento HTML
//OK focus -> se desencadena cuando un elemento de HTML, recibe el foco , es decir cuando se selecciona o se enfoca
//OK blur -> se desencadena cuando un elemento de HTML, pierde el foco , es decir cuando se deselecciona o se desenfoca
//OK mouseover -> se desencadena cuando el cursor del raton se coloca sobre un elemento de HTML
//OK mouseout -> se activa cuando el cursor del raton sale del elemento HTML
//OK select -> ocurre cuando se selecciona parte o todo el texto de un elemento de entrada (input) o area de text (textarea)
//OK contextmenu -> se activa cuando se hace click derecho con el boton del mouse sobre un elemento HTML